# Assignment Operator
a = 10

# Addition Assignment
a += 5
print ("a += 5 : ", a)  # 10 + 5


# Subtraction Assignment
a -= 5
print ("a -= 5 : ", a) # 15 - 5

# Multiplication Assignment
a *= 5
print ("a *= 5 : ", a) # 10 * 5

# Division Assignment
a /= 5
print ("a /= 5 : ",a) # 50 / 5

# Remainder Assignment
a %= 3
print ("a %= 3 : ", a) # 10 % 3

# Exponent Assignment
a **= 2
print ("a **= 2 : ", a) # 1 + 2

# Floor Division Assignment
a //= 3
print ("a //= 3 : ", a) # 1 // 3