a = 10
b = 11
c = 10

# Equal
print ("a == b : ", a == b)  # False
print ("a == c : ", a == c)  # True


# Not Equal
print ("a != b : ", a != b)  # True
print ("a != c : ", a != c)  # False

# Greater Than
print ("a > b : ", a > b)  # False

# Less Than
print ("a < b : ", a < b)  # True

# Greater Than or Equal to
print ("a >= b : ", a >= b)  # False
print ("a >= c : ", a >= c)  # True

# Less Than or Equal to
print ("a <= b : ", a <= b)  # True