a = 16
b = 3

# Addition
print ("a + b : ", a + b)  # 16 + 3

# Subtraction
print ("a - b : ", a - b)  # 16 - 3

# Multiplication
print ("a * b : ", a * b)  # 16 * 3

# Division
print ("a / b : ", a / b)  # 16 / 3 

# Modulus
print ("a % b : ", a % b)  # 16 % 3 

# Exponent 
print ("a ** b : ", a ** b)  # 16 * 16 * 16

# Floor Division
print ("a // b : ", a // b)  # 16 % 3 but int value